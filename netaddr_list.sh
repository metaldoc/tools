#!/bin/sh

echo Public addresses:

ifconfig | grep 'inet' | grep -v '127.0.0.1' | egrep '\b((25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)(\.|$)){4}\b' | awk '{print $2}' | sed 's/addr://' | egrep -v '(^127\.0\.0\.1)|(^10\.)|(^172\.1[6-9]\.)|(^172\.2[0-9]\.)|(^172\.3[0-1]\.)|(^192\.168\.)'

echo Private addresses:

ifconfig | grep 'inet' | grep -v '127.0.0.1' | egrep '\b((25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)(\.|$)){4}\b' | awk '{print $2}' | sed 's/addr://' | egrep  '(^127\.0\.0\.1)|(^10\.)|(^172\.1[6-9]\.)|(^172\.2[0-9]\.)|(^172\.3[0-1]\.)|(^192\.168\.)'